<script>
  document.addEventListener("DOMContentLoaded", function() {
  // Configuración para computadoras
  if (window.innerWidth > 768) {
    var flktyDesktop = new Flickity('.carrusel_carteles', {
      cellAlign: 'left',
      contain: true,
      wrapAround: true,
      autoPlay: 3000,
      pauseAutoPlayOnHover: false,
      prevNextButtons: true,
      selectedAttraction: 0.01,
      friction: 0.2
    });

    flktyDesktop.on('select', function () {
      flktyDesktop.playPlayer();
    });

    var prevButton = document.querySelector('.flickity-prev-next-button.previous');
    var nextButton = document.querySelector('.flickity-prev-next-button.next');

    [prevButton, nextButton].forEach(function(button) {
      if (button) {
        button.addEventListener('click', function() {
          flktyDesktop.playPlayer();
        });
      }
    });
  }

  // Configuración para móviles
  if (window.innerWidth <= 768) {
    var flktyMobile = new Flickity('.carrusel_carteles-cel', {
      cellAlign: 'left',
      contain: true,
      wrapAround: true,
      autoPlay: 3000,
      pauseAutoPlayOnHover: false,
      prevNextButtons: true,
      selectedAttraction: 0.01,
      friction: 0.2
    });

    flktyMobile.on('select', function () {
      flktyMobile.playPlayer();
    });
  }

  // Manejo de miniaturas y modal
  const thumbnails = document.querySelectorAll('.child_carteles img, .child_carteles-cel img');
  const modal = document.getElementById('modalImagen');
  const modalImage = document.getElementById('imagenAmpliada');

  thumbnails.forEach((thumbnail) => {
    thumbnail.addEventListener('click', () => {
      modalImage.src = thumbnail.src;
      modal.style.display = 'flex';
      if (window.innerWidth > 768) {
        flktyDesktop.pausePlayer();
      } else {
        flktyMobile.pausePlayer();
      }
    });
  });

  modal.addEventListener('click', (event) => {
    if (event.target === modal) {
      modal.style.display = 'none';
      modalImage.src = '';
      if (window.innerWidth > 768) {
        flktyDesktop.playPlayer();
      } else {
        flktyMobile.playPlayer();
      }
    }
  });

  // Asegurar que autoplay se reanude tras interacción en móviles
  document.addEventListener('touchstart', () => {
    if (window.innerWidth <= 768) {
      flktyMobile.playPlayer();
    }
  });
});

</script>




